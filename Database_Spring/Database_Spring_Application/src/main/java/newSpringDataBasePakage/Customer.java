package newSpringDataBasePakage;

public class Customer {

		public String first;
		public String last;
		
		public void setFirst(String first) {
			this.first= first;
		}
		
		public void setLast(String last) {
			this.last= last;
		}
		
		public String getFirst() {
			return this.first;
		}
		
		public String getLast() {
			return this.last;
		}
}
